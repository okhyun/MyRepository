package ch12.exam05;

public class ThreadA extends Thread {
	public boolean stop = false;
	public boolean work = true;
	
	
	@Override
	public void run() {
		while(!stop) { //while문의 stop이  true일때
			if(work){
				System.out.println("ThreadA 작업 내용");
			} else {
				Thread.yield();
			}
		}
		System.out.println("ThreadA 종료");
	}

}
