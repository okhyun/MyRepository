package ch06.exam17;

public class Board {
	private int bnum;
	private String btitle;
	private String bname;
	private String btyping;
	private String bcount;
	
	public int getBnum() {
		return bnum;
	}
	public void setBnum(int bnum) {
		this.bnum = bnum;
	}
	public String getBtitle() {
		return btitle;
	}
	public void setBtitle(String btitle) {
		this.btitle = btitle;
	}
	public String getBname() {
		return bname;
	}
	public void setBname(String bname) {
		this.bname = bname;
	}
	public String getBtyping() {
		return btyping;
	}
	public void setBtyping(String btyping) {
		this.btyping = btyping;
	}
	public String getBcount() {
		return bcount;
	}
	public void setBcount(String bcount) {
		this.bcount = bcount;
	}

}
